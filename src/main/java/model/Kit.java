package model;

public class Kit {
	public String kit;
	public String description;
	
	public String getKit() {
		return kit;
	}
	
	public void setKit(String kit) {
		this.kit = kit;
	}
	
	public String getDescription() {
		return description;
	}
	
	public void setDescription(String description) {
		this.description = description;
	}
}
